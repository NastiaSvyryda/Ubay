drop table if exists users;
drop table if exists lots;
drop table if exists bids;

CREATE TABLE IF NOT EXISTS users (id INTEGER NOT NULL AUTO_INCREMENT,login VARCHAR(64),password VARCHAR(64),userRole INTEGER,PRIMARY KEY (id));

CREATE TABLE IF NOT EXISTS lots (id INTEGER NOT NULL AUTO_INCREMENT,sellerId INTEGER NOT NULL,title VARCHAR(64),startPrice INTEGER,FOREIGN KEY (sellerId) REFERENCES users(id) ON DELETE CASCADE,PRIMARY KEY (id));

CREATE TABLE IF NOT EXISTS bids (id INTEGER NOT NULL AUTO_INCREMENT,lotId INTEGER,bidderId INTEGER,newPrice INTEGER,statusOfBid INTEGER,FOREIGN KEY (bidderId) REFERENCES users(id) ON DELETE CASCADE,FOREIGN KEY (lotId) REFERENCES lots(id) ON DELETE CASCADE,PRIMARY KEY (id));
